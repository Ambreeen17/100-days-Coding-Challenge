// This funcion mixes a text and a  number into one text function
function combineStringAndNumber(text, number) {
    //join the text and number into a single text
    return text + number;
}
console.log(combineStringAndNumber("Age:", 20)); //Age:20
function combineString_Number(text1, number, text2) {
    return text1 + number + text2;
}
console.log(combineString_Number("Probook", 6560, "b")); //Probook6560b
